typedef long long ll;

const long long MOD = 1000000007;

void solve(long long N, std::vector<long long> A)
{
	ll dp[100010][2] = {}, way[100010][2] = {};

	way[1][0] = 1;
	dp[1][0] = A[0];

	for (int i = 1; i < N; i++)
	{
		dp[i + 1][0] = dp[i][0] + A[i] * way[i][0] + dp[i][1] + A[i] * way[i][1];
		dp[i + 1][1] = dp[i][0] - A[i] * way[i][0];
		way[i + 1][0] = way[i][0] + way[i][1];
		way[i + 1][1] = way[i][0];

		dp[i + 1][0] %= MOD;
		dp[i + 1][1] %= MOD;
		way[i + 1][0] %= MOD;
		way[i + 1][1] %= MOD;
	}
	cout << (dp[N][0] + dp[N][1] + MOD) % MOD << endl;
}

